/** Specifies the key codes for the navigation keys. */
export enum NavigationKeyCode {
  /** The `Arrow Left` key. */
  ArrowLeft = 37,

  /** The `Arrow Up` key. */
  ArrowUp = 38,

  /** The `Arrow Right` key. */
  ArrowRight = 39,

  /** The `Arrow Down` key. */
  ArrowDown = 40,

  /** The `Home` key. */
  Home = 36,

  /** The `End` key. */
  End = 35,

  /** The `Page Up` key. */
  PageUp = 33,

  /** The `Page Down` key. */
  PageDown = 34,
}
