/** Specifies the console color code integers. */
export enum ConsoleColor {
  /** A black foreground. */
  FgBlack = 30,

  /** A red foreground. */
  FgRed = 31,

  /** A green foreground. */
  FgGreen = 32,

  /** A yellow foreground. */
  FgYellow = 33,

  /** A blue foreground. */
  FgBlue = 34,

  /** A magenta foreground. */
  FgMagenta = 35,

  /** A cyan foreground. */
  FgCyan = 36,

  /** A white foreground. */
  FgWhite = 37,

  /** A gray foreground. */
  FgGray = 90,

  /** A black background. */
  BgBlack = 40,

  /** A red background. */
  BgRed = 41,

  /** A green background. */
  BgGreen = 42,

  /** A yellow background. */
  BgYellow = 43,

  /** A blue background. */
  BgBlue = 44,

  /** A magenta background. */
  BgMagenta = 45,

  /** A cyan background. */
  BgCyan = 46,

  /** A white background. */
  BgWhite = 47,

  /** A gray background. */
  BgGray = 100,
}
