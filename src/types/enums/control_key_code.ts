/** Specifies the control key codes. */
export enum ControlKeyCode {
  /** The `Caps Lock` key. */
  CapsLock = 20,

  /** The `Escape` or `Esc` key. */
  Escape = 27,

  /** The `Scroll Lock` key. */
  ScrollLock = 145,

  /** The `Num Lock` key. */
  NumLock = 144,

  /** The `Print Screen` key. */
  PrintScreen = 44,

  /** The `Pause` key. */
  Pause = 19,

  /** The `Insert` key. */
  Insert = 45,

  /** The `Control` or `Ctrl` key. */
  Control = 17,

  /** The `Alt` key. */
  Alt = 18,

  /** The `Shift` key. */
  Shift = 16,

  /** The `ContextMenu` key. */
  ContextMenu = 93,

  /** The `Meta` key. */
  Meta = 91,

  /** The `Cancel` key. */
  Cancel = 3,
}
