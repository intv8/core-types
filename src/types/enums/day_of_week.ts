/** Specifies the zero-indexed days of the week. */
export enum DayOfWeek {
  /** Zero-indexed Sunday. */
  Sunday = 0,

  /** Zero-indexed Monday. */
  Monday = 1,

  /** Zero-indexed Tuesday. */
  Tuesday = 2,

  /** Zero-indexed Wednesday. */
  Wednesday = 3,

  /** Zero-indexed Thursday. */
  Thursday = 4,

  /** Zero-indexed Friday. */
  Friday = 5,

  /** Zero-indexed Saturday. */
  Saturday = 6,
}
