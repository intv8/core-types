/** Specifies the key codes for the media keys. */
export enum MediaKeyCode {
  /** The `Media Stop` key. */
  Stop = 178,

  /** The `Media Play/Pause` key. */
  PlayPause = 179,

  /** The `Media Previous Track` key. */
  PreviousTrack = 177,

  /** The `Media Next Track` key. */
  NextTrack = 176,
}
